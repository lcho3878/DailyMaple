import SwiftUI

extension Font {    
    static func mapleBold(_ size: CGFloat) -> Font {
        return .custom("Maplestory OTF Bold", size: size)
    }
    
    static func mapleLight(_ size: CGFloat) -> Font {
        return .custom("Maplestory OTF Light", size: size)
    }
}
